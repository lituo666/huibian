DATAS SEGMENT
STR1 DB 'hellowo'
STR2 DB 'hellowo'
STR3 DB 'MATCH$'
STR4 DB 'NOMATCH$'
DATAS ENDS

CODES SEGMENT
MAIN PROC FAR
	ASSUME CS:CODES,DS:DATAS,ES:DATAS
START:	PUSH DS
		MOV AX,0
		PUSH AX
		MOV AX,DATAS
		MOV DS,AX
		MOV ES,AX
		
		MOV CX,7
		MOV SI,OFFSET STR1
		MOV DI,OFFSET STR2
		REPZ CMPSB		;STR1与SRT2相减，ZF=0即有不同字符时结束循环
		JZ LABZ
		JNZ LABNZ
	LABZ:
		MOV DX,OFFSET STR3
		MOV AH,9
		INT 21H
		JMP NEXT
		JNZ LABNZ
	LABNZ:
		MOV DX,OFFSET STR4
		MOV AH,9
		INT 21H
		JMP NEXT
	NEXT:
		RET
	MAIN ENDP
CODES ENDS
	END START


