DATA SEGMENT
	STR1 DB 'DVJOAAaaa-1'
	LENTH DB 0
DATA ENDS

CODE SEGMENT
	MAIN PROC FAR
	ASSUME CS:CODE,DS:DATA
	START:
		PUSH DS
		MOV AX,0
		PUSH AX
		
		MOV AX,DATA
		MOV DS,AX
		
		MOV BX,OFFSET STR1
		MOV DI,OFFSET LENTH
		
		CALL TJ
		MOV CX,8
	J4:
		SHL BYTE PTR[DI],1
		JC ONE
		MOV DL,30H
		JMP PRINT
	ONE:
		MOV DL,31H
	PRINT:
		MOV AH,2
		INT 21H
		DEC CX
		JNZ J4
		
		RET
		TJ PROC NEAR
		J1:
			CMP BYTE PTR[BX],'-'
			JNZ J3
			CMP BYTE PTR[BX+1],'1'
			JNZ J3
			RET
		J3:
			CMP BYTE PTR[BX],'A'
			JB J2
			CMP BYTE PTR[BX],'Z'
			JA J2
			INC BYTE PTR[DI]
			
		J2:
			INC BX
			JMP J1
			
		TJ ENDP
		
	MAIN ENDP
CODE ENDS
	END START



